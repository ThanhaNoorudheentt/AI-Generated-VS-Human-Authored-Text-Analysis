# Helper functions for analysis
def analyze_signatures(text):
    # Formal transitions (AI signatures)
    formal_words = ['however', 'consequently', 'furthermore', 'nevertheless', 'overall', 'in conclusion', 'moreover']
    formal_count = sum(1 for word in formal_words if re.search(rf'\b{word}\b', text.lower()))
    
    # Personal pronouns (Human signatures)
    personal_pronouns = ['i', 'my', 'me', 'we', 'our', 'us', 'you', 'your']
    human_count = sum(1 for word in personal_pronouns if re.search(rf'\b{word}\b', text.lower()))
    
    # Informal fillers
    informal_words = ['basically', 'literally', 'honestly', 'pretty', 'cool', 'stuff', 'vibes', 'bro']
    informal_count = sum(1 for word in informal_words if re.search(rf'\b{word}\b', text.lower()))
    
    return {
        'formal_score': formal_count,
        'human_score': human_count + informal_count
    }

# Load model and vectorizer
@st.cache_resource
def load_artifacts():
    try:
        model_path = os.path.join(os.path.dirname(__file__), 'lr_model.joblib')
        vectorizer_path = os.path.join(os.path.dirname(__file__), 'tfidf_vectorizer.joblib')
        model = joblib.load(model_path)
        vectorizer = joblib.load(vectorizer_path)
        return model, vectorizer
    except:
        return None, None

model, vectorizer = load_artifacts()

