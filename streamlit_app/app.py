            X = vectorizer.transform([input_text])
            probs = model.predict_proba(X)[0]
            prediction = model.predict(X)[0]
            confidence = probs[prediction]
            
            # Display Result
            st.markdown('<div class="custom-card">', unsafe_allow_html=True)
            if prediction == 1:
                st.markdown('<div class="result-badge ai-badge">🤖 CLASSIFIED AS AI-GENERATED</div>', unsafe_allow_html=True)
                st.write(f"The model is **{confidence:.1%} confident** that this text was generated by an LLM.")
